package com.ihg.grs.bean;

/**
 * A POJO for Site Search functionality.journal article, Document, Web Content.
 * @author nakul.patel
 *
 */
public class SearchVO 
{
	private String title;
	private String desc;
	private String url;
	private String id;
	private String date;
	private String type;
	private String imageUrl;
	
	/**
	 * Returns URL of image.
	 * @return imageUrl as String.
	 */
	public String getImageUrl()
	{
		return imageUrl;
	}
	
	/**
	 * Sets image URL.
	 * @param imageUrl Sets image URL for search result.
	 */
	public void setImageUrl(String imageUrl)
	{
		this.imageUrl = imageUrl;
	}
	
	/**
	 * Returns title of the search results.
	 * @return title as String.
	 */
	public String getTitle()
	{
		return title;
	}
	
	/**
	 * Sets title of the search result.
	 * @param title as String.
	 */
	public void setTitle(String title)
	{
		this.title = title;
	}
	
	/**
	 * Returns description of the serach result found.
	 * @return desc as String.
	 */
	public String getDesc()
	{
		return desc;
	}
	
	/**
	 * Sets description to the search result.
	 * @param desc as String.
	 */
	public void setDesc(String desc)
	{
		this.desc = desc;
	}
	
	/**
	 * Returns download URL of the search results.
	 * @return url as String.
	 */
	public String getUrl()
	{
		return url;
	}
	
	/**
	 * Sets downloadable url to the search result.
	 * @param url as String.
	 */
	public void setUrl(String url)
	{
		this.url = url;
	}
	
	/**
	 * Returns id of the search result.
	 * @return id as String.
	 */
	public String getId()
	{
		return id;
	}
	
	/**
	 * Sets id to the search result.
	 * @param id as String
	 */
	public void setId(String id)
	{
		this.id = id;
	}
	
	/**
	 * Returns date of the search result object.
	 * @return date as String.
	 */
	public String getDate()
	{
		return date;
	}
	
	/**
	 * Sets date to the search result object.
	 * @param date as String.
	 */
	public void setDate(String date)
	{
		this.date = date;
	}
	
	/**
	 * Returns type of the search result object.
	 * @return type as String.
	 */
	public String getType()
	{
		return type;
	}
	
	/**
	 * Sets type to the search result.
	 * @param type as String.
	 */
	public void setType(String type)
	{
		this.type = type;
	}
	
	
}
