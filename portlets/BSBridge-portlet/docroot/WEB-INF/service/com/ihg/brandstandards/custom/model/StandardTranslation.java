package com.ihg.brandstandards.custom.model;

import java.util.ArrayList;
import java.util.List;

public class StandardTranslation extends StandardBase
{
    private String localeCode;
    private List<StandardTranslation> specifications = new ArrayList<StandardTranslation>();
    private List<StandardTranslation> guidelines = new ArrayList<StandardTranslation>();
    
    public StandardTranslation()
    {
    }
    
    /**
     * @return the localeCode
     */
    public String getLocaleCode()
    {
        return localeCode;
    }
    
    /**
     * @param localeCode the localeCode to set
     */
    public void setLocaleCode(String localeCode)
    {
        this.localeCode = localeCode;
    }
    
    /**
     * @return the specifications
     */
    public List<StandardTranslation> getSpecifications()
    {
        return specifications;
    }
    
    /**
     * @param specifications the specifications to set
     */
    public void setSpecifications(List<StandardTranslation> specifications)
    {
        this.specifications = specifications;
    }
    
    /**
     * @return the guidelines
     */
    public List<StandardTranslation> getGuidelines()
    {
        return guidelines;
    }
    
    /**
     * @param guidelines the guidelines to set
     */
    public void setGuidelines(List<StandardTranslation> guidelines)
    {
        this.guidelines = guidelines;
    }
    
}
