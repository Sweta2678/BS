package com.ihg.me2.brandstandards.custom.pojo;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

import com.ihg.brandstandards.custom.model.Standard;

/**
 * <a href="Taxonomy.java.html"><b><i>View Source</i></b></a>.
 * 
 * @project: Brand Standards.
 * @author chintan.akhani
 */
public class Taxonomy implements Serializable
{
    private long indexOrder;
    private long taxonomyId;
    private long parentTaxonomyId;
    private long indexSortOrder;
    private long publishId;
    private String titleText;
    private String taxonomyPath;
    private String level;
    private List<Standard> standardsList;

    /**
     * @return long
     */
    public long getIndexOrder()
    {
        return indexOrder;
    }

    /**
     * @param indexOrder This is a long object
     */
    public void setIndexOrder(long indexOrder)
    {
        this.indexOrder = indexOrder;
    }

    /**
     * @return long
     */
    public long getTaxonomyId()
    {
        return taxonomyId;
    }

    /**
     * @param taxonomyId This is a long object
     */
    public void setTaxonomyId(long taxonomyId)
    {
        this.taxonomyId = taxonomyId;
    }

    /**
     * @return long
     */
    public long getParentTaxonomyId()
    {
        return parentTaxonomyId;
    }

    /**
     * @param parentTaxonomyId This is a long object
     */
    public void setParentTaxonomyId(long parentTaxonomyId)
    {
        this.parentTaxonomyId = parentTaxonomyId;
    }

    /**
     * @return String
     */
    public String getLevel()
    {
        return level;
    }

    /**
     * @param level This is a String object
     */
    public void setLevel(String level)
    {
        this.level = level;
    }

    /**
     * @return long
     */
    public long getIndexSortOrder()
    {
        return indexSortOrder;
    }

    /**
     * @param indexSortOrder This is a long object
     */
    public void setIndexSortOrder(long indexSortOrder)
    {
        this.indexSortOrder = indexSortOrder;
    }

    /**
     * @return long
     */
    public long getPublishId()
    {
        return publishId;
    }

    /**
     * @param publishId This is a long object
     */
    public void setPublishId(long publishId)
    {
        this.publishId = publishId;
    }

    /**
     * @return String
     */
    public String getTitleText()
    {
        return titleText;
    }

    /**
     * @param titleText This is a String object
     */
    public void setTitleText(String titleText)
    {
        this.titleText = titleText;
    }

    /**
     * @return String
     */
    public String getTaxonomyPath()
    {
        return taxonomyPath;
    }

    /**
     * @param taxonomyPath This is a String object
     */
    public void setTaxonomyPath(String taxonomyPath)
    {
        this.taxonomyPath = taxonomyPath;
    }

    /**
     * @return List<Standards>
     */
    public List<Standard> getStandardsList()
    {
        if(standardsList == null){
            standardsList = new ArrayList<Standard>();
        }
        return standardsList;
    }

    /**
     * @param standardsList This is a List<Standards> object
     */
    public void setStandardsList(List<Standard> standardsList)
    {
        this.standardsList = standardsList;
    }

}
