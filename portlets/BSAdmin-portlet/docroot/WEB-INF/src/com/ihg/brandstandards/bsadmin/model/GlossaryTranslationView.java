import com.ihg.brandstandards.db.model.StandardsLocale;

public class GlossaryTranslationView
{

    public Glossary glossary;
    
    public Map<StandardsLocale, GlossaryTranslate> glossaryTranslations;
    
    public Glossary getGlossary()
    {
        return glossary;
    }
    public void setGlossary(Glossary glossary)
    {
        this.glossary = glossary;
    }
    public Map<StandardsLocale, GlossaryTranslate> getGlossaryTranslations()
    {
        return glossaryTranslations;
    }
    public void setGlossaryTranslations(Map<StandardsLocale, GlossaryTranslate> glossaryTranslations)
    {
        this.glossaryTranslations = glossaryTranslations;
    }
}
